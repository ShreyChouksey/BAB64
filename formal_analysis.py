"""
Formal Security Analysis — Lemma 1: S-Box Indistinguishability
================================================================

LEMMA 1. The family of S-boxes generated by BAB64's Fisher-Yates
construction (seeded by SHA-256(image || b'sbox')) is computationally
indistinguishable from the uniform distribution on permutations of
[0..255].

PROOF METHOD. For each statistical criterion, we compute the test
statistic on both:
  (A) BAB64 S-boxes — generated by the actual construction
  (B) Reference permutations — generated by numpy's random.permutation
      (which uses rejection sampling for uniform indices)

We then apply a two-sample test (Kolmogorov-Smirnov or Mann-Whitney)
to determine whether the two distributions differ. This approach is
more rigorous than comparing against theoretical values because it
accounts for finite-sample effects and avoids errors in theoretical
baselines (e.g., E[max|LAT|] for random permutations is ~35, not 16
which is the AES-optimized minimum).

The argument rests on three pillars:
  1. The seed SHA-256(image || b'sbox') is indistinguishable from
     uniform under the random oracle model for SHA-256.
  2. Fisher-Yates with a uniform byte stream produces each
     permutation with equal probability (Knuth, TAOCP Vol. 2).
  3. The modular reduction (j = byte % (i+1)) introduces bias
     at most 1/256 per swap — we test whether this accumulates
     to a detectable level.

Six tests below validate pillars (1)-(3) empirically.

Target: IACR resubmission, Section 4 (Security Analysis).
"""


# =============================================================================
# S-BOX GENERATION
# =============================================================================

def generate_bab64_sbox(seed_index: int) -> np.ndarray:
    """Generate a BAB64 S-box from a deterministic image seed."""
    config = BAB64Config()
    renderer = BabelRenderer(config)
    seed = hashlib.sha256(f"formal_{seed_index}".encode()).digest()
    image = renderer.render(seed)
    hasher = ImageHash(config)
    return hasher._derive_sbox(image)


def generate_reference_sbox(rng: np.random.Generator) -> np.ndarray:
    """Generate a truly uniform random permutation (rejection sampling)."""
    return rng.permutation(256).astype(np.uint8)


# =============================================================================
# TEST 1 — NIST RANDOMNESS TESTS ON S-BOX OUTPUT SEQUENCES
# =============================================================================
# Rationale: Treat each S-box as a 256-byte sequence and apply runs
# and serial correlation tests. A uniformly random permutation of
# [0..255] has known statistics. Deviations indicate structure.
#
# For a random permutation of n elements:
#   Ascending runs: E = (2n-1)/3, Var = (16n-29)/90  [Knuth, TAOCP §3.3.2]
#   Serial correlation: E[r] = -1/(n-1), Var[r] ≈ 1/n

def count_ascending_runs(sbox: np.ndarray) -> int:
    """Count ascending runs in the sequence."""
    runs = 1
    for i in range(1, len(sbox)):
        if sbox[i] < sbox[i - 1]:
            runs += 1
    return runs


def serial_correlation(sbox: np.ndarray) -> float:
    """Pearson correlation between consecutive S-box entries."""
    x = sbox[:-1].astype(np.float64)
    y = sbox[1:].astype(np.float64)
    return float(np.corrcoef(x, y)[0, 1])


def test_nist_randomness(n_sboxes=1000):
    """
    TEST 1: Runs and serial correlation.
    Two-sample KS test: BAB64 vs reference permutations.
    """
    rng = np.random.default_rng(42)

    bab_runs = []
    ref_runs = []
    bab_corr = []
    ref_corr = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)

        bab_runs.append(count_ascending_runs(bab_sb))
        ref_runs.append(count_ascending_runs(ref_sb))
        bab_corr.append(serial_correlation(bab_sb))
        ref_corr.append(serial_correlation(ref_sb))

    runs_ks, runs_p = sp_stats.ks_2samp(bab_runs, ref_runs)
    corr_ks, corr_p = sp_stats.ks_2samp(bab_corr, ref_corr)

    return {
        'runs': {
            'bab_mean': np.mean(bab_runs), 'ref_mean': np.mean(ref_runs),
            'ks_stat': runs_ks, 'p_value': runs_p,
        },
        'serial': {
            'bab_mean': np.mean(bab_corr), 'ref_mean': np.mean(ref_corr),
            'ks_stat': corr_ks, 'p_value': corr_p,
        },
    }


# =============================================================================
# TEST 2 — ALGEBRAIC DEGREE (ANF) OF COMPONENT BOOLEAN FUNCTIONS
# =============================================================================
# Rationale: Each 8-bit S-box defines 8 component Boolean functions
# f_i(x) = bit_i(S(x)). The algebraic normal form (ANF) gives the
# unique representation as a polynomial over GF(2). We compare the
# degree distribution of BAB64 vs reference permutations.
#
# Method: Moebius transform to compute ANF, then read off degree.
# Failure mode: if BAB64 consistently shows lower degree, algebraic
# attacks (Courtois-Pieprzyk, 2002) become feasible.

def moebius_transform(truth_table: np.ndarray) -> np.ndarray:
    """Compute ANF via the Moebius transform over GF(2)."""
    n = int(math.log2(len(truth_table)))
    anf = truth_table.copy().astype(np.uint8)
    for i in range(n):
        step = 1 << i
        for j in range(0, 1 << n):
            if j & step:
                anf[j] ^= anf[j ^ step]
    return anf


def algebraic_degree(sbox: np.ndarray) -> list:
    """Compute algebraic degree of each of the 8 component functions."""
    degrees = []
    for bit in range(8):
        tt = np.array([(int(sbox[x]) >> bit) & 1 for x in range(256)],
                      dtype=np.uint8)
        anf = moebius_transform(tt)
        max_deg = 0
        for m in range(256):
            if anf[m]:
                deg = bin(m).count('1')
                if deg > max_deg:
                    max_deg = deg
        degrees.append(max_deg)
    return degrees


def min_algebraic_degree(sbox: np.ndarray) -> int:
    """Minimum algebraic degree across all component functions."""
    return min(algebraic_degree(sbox))


def test_algebraic_degree(n_sboxes=100):
    """
    TEST 2: Algebraic degree — two-sample comparison.
    Mann-Whitney U test on minimum component degree.
    """
    rng = np.random.default_rng(43)

    bab_min_deg = []
    ref_min_deg = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)
        bab_min_deg.append(min_algebraic_degree(bab_sb))
        ref_min_deg.append(min_algebraic_degree(ref_sb))

    stat, p_value = sp_stats.mannwhitneyu(
        bab_min_deg, ref_min_deg, alternative='two-sided')

    bab_all = []
    ref_all = []
    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)
        bab_all.extend(algebraic_degree(bab_sb))
        ref_all.extend(algebraic_degree(ref_sb))

    bab_all = np.array(bab_all)
    ref_all = np.array(ref_all)

    return {
        'bab_frac_7': np.mean(bab_all == 7),
        'ref_frac_7': np.mean(ref_all == 7),
        'bab_min_mean': np.mean(bab_min_deg),
        'ref_min_mean': np.mean(ref_min_deg),
        'u_stat': stat,
        'p_value': p_value,
    }


# =============================================================================
# TEST 3 — LINEAR APPROXIMATION TABLE (LAT)
# =============================================================================
# Rationale: LAT[a,b] = #{x : a·x ⊕ b·S(x) = 0} - 128.
# The max |LAT[a,b]| for a,b ≠ 0 determines resistance to linear
# cryptanalysis (Matsui, 1993).
#
# For random permutations of [0..255]:
#   E[max|LAT|] ≈ 2^(n/2) * sqrt(n*ln 2) ≈ 34-38  [O'Connor 1994]
# (NOT 16 — that is the AES-optimized minimum.)
#
# We compare BAB64 vs reference permutations via two-sample KS.

def compute_lat_max_bias(sbox: np.ndarray) -> int:
    """Compute max |bias| in the LAT, excluding trivial row/col 0."""
    n = 256
    max_bias = 0

    # Precompute parity lookup
    parity = np.zeros(n, dtype=np.int32)
    for x in range(n):
        parity[x] = bin(x).count('1') & 1

    for a in range(1, n):
        # Compute a·x for all x
        ax_parity = np.array([parity[a & x] for x in range(n)])
        for b in range(1, n):
            # Compute b·S(x) for all x
            bsx_parity = np.array([parity[b & sbox[x]] for x in range(n)])
            # Count matches
            matches = int(np.sum(ax_parity == bsx_parity))
            bias = abs(matches - 128)
            if bias > max_bias:
                max_bias = bias

    return max_bias


def test_lat(n_sboxes=50):
    """
    TEST 3: LAT max bias — two-sample comparison.
    KS test: BAB64 max_bias distribution vs reference.
    """
    rng = np.random.default_rng(44)

    bab_biases = []
    ref_biases = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)

        bab_biases.append(compute_lat_max_bias(bab_sb))
        ref_biases.append(compute_lat_max_bias(ref_sb))

        if (i + 1) % 10 == 0:
            print(f"    LAT: {i+1}/{n_sboxes} done", flush=True)

    ks_stat, p_value = sp_stats.ks_2samp(bab_biases, ref_biases)

    return {
        'bab_mean': np.mean(bab_biases),
        'ref_mean': np.mean(ref_biases),
        'bab_std': np.std(bab_biases),
        'ref_std': np.std(ref_biases),
        'ks_stat': ks_stat,
        'p_value': p_value,
    }


# =============================================================================
# TEST 4 — DIFFERENTIAL DISTRIBUTION TABLE (DDT)
# =============================================================================
# Rationale: DDT[Δx, Δy] = #{x : S(x) ⊕ S(x⊕Δx) = Δy}.
# Max DDT entry = differential uniformity. Lower is better.
# For random permutations: E[max DDT] ≈ 10-12 for 8-bit.
# AES: max DDT = 4 (optimal). We compare BAB64 vs reference.

def compute_ddt_max(sbox: np.ndarray) -> int:
    """Compute max entry in the DDT (differential uniformity)."""
    max_val = 0
    for dx in range(1, 256):
        counts = np.zeros(256, dtype=np.int32)
        for x in range(256):
            dy = sbox[x] ^ sbox[x ^ dx]
            counts[dy] += 1
        c = int(np.max(counts))
        if c > max_val:
            max_val = c
    return max_val


def test_ddt(n_sboxes=100):
    """
    TEST 4: DDT max entry — two-sample comparison.
    KS test: BAB64 differential uniformity vs reference.
    """
    rng = np.random.default_rng(45)

    bab_du = []
    ref_du = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)

        bab_du.append(compute_ddt_max(bab_sb))
        ref_du.append(compute_ddt_max(ref_sb))

        if (i + 1) % 20 == 0:
            print(f"    DDT: {i+1}/{n_sboxes} done", flush=True)

    ks_stat, p_value = sp_stats.ks_2samp(bab_du, ref_du)

    return {
        'bab_mean': np.mean(bab_du),
        'ref_mean': np.mean(ref_du),
        'ks_stat': ks_stat,
        'p_value': p_value,
    }


# =============================================================================
# TEST 5 — FIXED-POINT-FREE FRACTION (DERANGEMENTS)
# =============================================================================
# Rationale: For uniform random permutations of [n]:
#   P(derangement) → 1/e ≈ 0.3679
#   #fixed_points ~ Poisson(1)
#
# The Fisher-Yates modular reduction (j = byte % (i+1)) introduces
# bias. If this accumulates, the fixed-point distribution will shift.
# We compare BAB64 against reference permutations.

def count_fixed_points(sbox: np.ndarray) -> int:
    return int(np.sum(sbox == np.arange(256, dtype=np.uint8)))


def test_fixed_points(n_sboxes=10000):
    """
    TEST 5: Fixed-point distribution — two-sample comparison.
    (a) KS test on fixed-point count distributions.
    (b) Report derangement fractions for both.
    """
    rng = np.random.default_rng(46)

    bab_fp = []
    ref_fp = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)
        bab_fp.append(count_fixed_points(bab_sb))
        ref_fp.append(count_fixed_points(ref_sb))

    bab_fp = np.array(bab_fp)
    ref_fp = np.array(ref_fp)

    ks_stat, p_value = sp_stats.ks_2samp(bab_fp, ref_fp)

    return {
        'bab_derangement_frac': np.mean(bab_fp == 0),
        'ref_derangement_frac': np.mean(ref_fp == 0),
        'expected_frac': 1.0 / math.e,
        'bab_fp_mean': bab_fp.mean(),
        'ref_fp_mean': ref_fp.mean(),
        'ks_stat': ks_stat,
        'p_value': p_value,
    }


# =============================================================================
# TEST 6 — CYCLE STRUCTURE
# =============================================================================
# Rationale: For a uniform random permutation of [n]:
#   E[#cycles] = H_n ≈ ln(n) + γ ≈ 6.12  for n=256
#   E[longest cycle] ≈ 0.6243 * n ≈ 159.8  (Golomb-Dickman constant)

def decompose_cycles(sbox: np.ndarray) -> list:
    """Decompose permutation into cycle lengths."""
    n = len(sbox)
    visited = np.zeros(n, dtype=bool)
    cycle_lengths = []
    for start in range(n):
        if visited[start]:
            continue
        length = 0
        x = start
        while not visited[x]:
            visited[x] = True
            x = int(sbox[x])
            length += 1
        cycle_lengths.append(length)
    return cycle_lengths


def test_cycle_structure(n_sboxes=1000):
    """
    TEST 6: Cycle structure — two-sample comparison.
    (a) KS test on number of cycles.
    (b) KS test on longest cycle length.
    """
    rng = np.random.default_rng(47)

    bab_num_cycles = []
    ref_num_cycles = []
    bab_longest = []
    ref_longest = []

    for i in range(n_sboxes):
        bab_sb = generate_bab64_sbox(i)
        ref_sb = generate_reference_sbox(rng)

        bab_c = decompose_cycles(bab_sb)
        ref_c = decompose_cycles(ref_sb)

        bab_num_cycles.append(len(bab_c))
        ref_num_cycles.append(len(ref_c))
        bab_longest.append(max(bab_c))
        ref_longest.append(max(ref_c))

    ks_cycles, p_cycles = sp_stats.ks_2samp(bab_num_cycles, ref_num_cycles)
    ks_longest, p_longest = sp_stats.ks_2samp(bab_longest, ref_longest)

    return {
        'bab_mean_cycles': np.mean(bab_num_cycles),
        'ref_mean_cycles': np.mean(ref_num_cycles),
        'ks_cycles': ks_cycles,
        'p_cycles': p_cycles,
        'bab_mean_longest': np.mean(bab_longest),
        'ref_mean_longest': np.mean(ref_longest),
        'ks_longest': ks_longest,
        'p_longest': p_longest,
    }


